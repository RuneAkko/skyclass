package com.android.domain;

public class NewsDataFile {
	private String content=null;
	
	
	
	public NewsDataFile() {
		super();
	}
	public NewsDataFile(String content) {
		super();
		this.content = content;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}
